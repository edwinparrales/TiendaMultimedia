    private void btnAgragarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgragarCarritoActionPerformed
       
        String pre;
        
        String data []=new String[4];
        int fila = tablaBuscar.getSelectedRow();
        if (fila>=0) {
            data[0]=tablaBuscar.getValueAt(fila, 0).toString();
            data[1]=tablaBuscar.getValueAt(fila, 1).toString();
            data[2]=tablaBuscar.getValueAt(fila, 2).toString();
            data[3]=tablaBuscar.getValueAt(fila, 3).toString();
            pre=tablaBuscar.getValueAt(fila, 3).toString();
            modcarrito.addRow(data);
            tablacarrito.setModel(modcarrito);
            total+=Double.parseDouble(pre);
            totalCarrito.setText(""+total);
            can=modcarrito.getRowCount();
            cantCarrito.setText(""+can);
        } else {
            JOptionPane.showMessageDialog(null," DEBE SELECCIONAR UN ITEM ","ALERTA",JOptionPane.WARNING_MESSAGE);
        }
        
        
        
        
        
    }//GEN-LAST:event_btnAgragarCarritoActionPerformed

